function getCleanDescription(htmlString) {
    const tempDiv = document.createElement('div');
    tempDiv.innerHTML = htmlString;
    const firstParagraph = Array.from(tempDiv.querySelectorAll('p')).find(p => p.textContent.trim().length > 10);
    let text = (firstParagraph ? firstParagraph.textContent : tempDiv.textContent || "").trim();
    if (text.length < 20 || text.includes('episode of my podcast')) {
        const currentLang = document.documentElement.lang || 'en';
        return translations[currentLang].episodeDefaultDesc;
    }
    if (text.length > 150) {
        return text.substring(0, 147) + '...';
    }
    return text;
}

// --- Helper to slugify titles for linking ---
function slugify(text) {
    return text.toString().toLowerCase().trim()
        .replace(/\s+/g, '-')           // Replace spaces with -
        .replace(/[^\w\u0400-\u04FF-]+/g, '') // Remove all non-word chars (preserving Ukrainian cyrillic)
        .replace(/--+/g, '-')         // Replace multiple - with single -
        .replace(/^-+/, '')             // Trim - from start of text
        .replace(/-+$/, '');            // Trim - from end of text
}

// --- Share Episode Link ---
function shareEpisode(slug, buttonElement) {
    const shareUrl = `${window.location.origin}${window.location.pathname}#${slug}`;
    
    navigator.clipboard.writeText(shareUrl).then(() => {
        const originalText = buttonElement.innerHTML;
        const currentLang = document.documentElement.lang || 'en';
        const copiedText = currentLang === 'uk' ? 'Посилання скопійовано!' : 'Link copied!';
        
        buttonElement.innerHTML = `
            <svg xmlns="http://www.w3.org/2000/svg" class="h-4 w-4 inline mr-1 text-green-600" fill="none" viewBox="0 0 24 24" stroke="currentColor">
                <path stroke-linecap="round" stroke-linejoin="round" stroke-width="2" d="M5 13l4 4L19 7" />
            </svg>
            <span>${copiedText}</span>
        `;
        buttonElement.classList.add('text-green-600');
        buttonElement.disabled = true;
        
        setTimeout(() => {
            buttonElement.innerHTML = originalText;
            buttonElement.classList.remove('text-green-600');
            buttonElement.disabled = false;
        }, 2000);
    }).catch(err => {
        console.error("Failed to copy link: ", err);
    });
}

// --- Handle Hash Navigation ---
function handleHashNavigation() {
    const hash = window.location.hash.substring(1);
    if (!hash) return;

    // Find the episode index with this slug
    const index = allEpisodes.findIndex(ep => slugify(ep.title) === hash);
    if (index !== -1) {
        // Calculate the page index where the episode belongs
        const targetPage = Math.floor(index / EPISODES_PER_PAGE);
        if (currentEpisodePage !== targetPage) {
            currentEpisodePage = targetPage;
            displayVisibleEpisodes();
        }

        setTimeout(() => {
            const element = document.getElementById(hash);
            if (element) {
                element.scrollIntoView({ behavior: 'smooth', block: 'center' });
                // Add highlight ring
                element.classList.add('ring-4', 'ring-blue-500', 'ring-opacity-50');
                setTimeout(() => {
                    element.classList.remove('ring-4', 'ring-blue-500', 'ring-opacity-50');
                }, 3000);
            }
        }, 300);
    }
}

async function loadEpisodes() {
    const loader = document.getElementById('episodes-loader');
    const rssFeedUrl = 'https://anchor.fm/s/2ea2228/podcast/rss';
    const localFeedUrl = './episodes.xml';
    
    // Fallback Proxies
    const proxies = [
        `https://corsproxy.io/?${encodeURIComponent(rssFeedUrl)}`,
        `https://api.allorigins.win/raw?url=${encodeURIComponent(rssFeedUrl)}`,
        `https://thingproxy.freeboard.io/fetch/${rssFeedUrl}`
    ];

    let xmlText = null;

    // Try local cached feed first (no CORS)
    try {
        console.log("Attempting to load local episodes.xml...");
        const response = await fetch(localFeedUrl);
        if (response.ok) {
            xmlText = await response.text();
            console.log("Successfully loaded episodes from local cache.");
        } else {
            console.warn(`Local episodes.xml not found or failed to load: status ${response.status}`);
        }
    } catch (localError) {
        console.warn("Failed to load local episodes.xml, will try fallback proxies:", localError);
    }

    // If local load failed, try fallback proxies sequentially
    if (!xmlText) {
        for (const proxyUrl of proxies) {
            try {
                console.log(`Attempting to fetch live episodes via proxy: ${proxyUrl}...`);
                const response = await fetch(proxyUrl);
                if (response.ok) {
                    xmlText = await response.text();
                    console.log("Successfully loaded episodes via proxy.");
                    break; // Success, stop trying other proxies
                } else {
                    console.warn(`Proxy failed: status ${response.status}`);
                }
            } catch (proxyError) {
                console.warn(`Proxy failed to fetch: ${proxyUrl}`, proxyError);
            }
        }
    }

    if (!xmlText) {
        console.error("All load attempts (local cache and fallback proxies) failed.");
        if (loader) loader.innerHTML = `<p class="text-center col-span-full text-red-500">Sorry, we couldn't load the episodes right now. This is likely due to network (CORS) restrictions. Please try again later.</p>`;
        return;
    }

    try {
        const parser = new DOMParser();
        const xmlDoc = parser.parseFromString(xmlText, "application/xml");

        const errorNode = xmlDoc.querySelector("parsererror");
        if (errorNode) {
            console.error("Parser Error:", errorNode.textContent);
            throw new Error("Error parsing XML feed.");
        }

        const items = xmlDoc.querySelectorAll("item");
        const parsedEpisodes = Array.from(items).map(item => {
            const enclosure = item.querySelector("enclosure");
            const itunesImage = item.querySelector("image[href], *[itunes\\:image][href]");

            return {
                title: item.querySelector("title")?.textContent || "",
                description: item.querySelector("description")?.textContent || "",
                pubDate: item.querySelector("pubDate")?.textContent || "",
                enclosure: {
                    url: enclosure?.getAttribute("url") || "",
                    type: enclosure?.getAttribute("type") || ""
                },
                thumbnail: itunesImage?.getAttribute("href") || null
            };
        });

        allEpisodes = parsedEpisodes.sort((a, b) => new Date(b.pubDate) - new Date(a.pubDate));
        currentEpisodePage = 0;
        displayVisibleEpisodes();
        handleHashNavigation();

    } catch (error) {
        console.error("Error processing RSS feed content:", error);
        if (loader) loader.innerHTML = `<p class="text-center col-span-full text-red-500">Sorry, we couldn't load the episodes right now. This is likely due to network (CORS) restrictions. Please try again later.</p>`;
    }
}

